class Post {
  final int id;
  final String author;
  final String district;
  final String postcode;
  final String time;
  final String type;
  final String title;
  final String content;
  final int likes;
  final int comments;
  final String avatar;

  Post({
    required this.id,
    required this.author,
    required this.district,
    required this.postcode,
    required this.time,
    required this.type,
    required this.title,
    required this.content,
    required this.likes,
    required this.comments,
    required this.avatar,
  });
}